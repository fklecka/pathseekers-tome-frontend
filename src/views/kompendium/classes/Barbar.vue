      <h3 class="text-2xl my-6 border-b">Klassenmerkmale</h3>
      <p class="pb-6">
        <strong>Umgang mit Waffen und Rüstungen:</strong> Ein Barbar ist im
        Umgang mit allen einfachen Waffen und Kriegswaffen, leichten Rüstungen,
        mittelschweren Rüstungen und Schilden (außer Turmschilden) geübt.
      </p>
      <p class="pb-6">
        <strong>Schnelle Bewegung (AF):</strong> Die Bewegung zu Land ist bei
        einem Barbaren höher als für sein Volk üblich. Er kann sich 3 m weiter
        bewegen. Dies gilt nur, wenn er gar keine, leichte oder mittelschwere
        Rüstung trägt und keine schwere Last mit sich führt. Dieser Bonus wird
        angewandt, ehe die Bewegungsrate des Barbaren durch getragene Lasten
        oder Rüstungen modifiziert wird, und er addiert sich mit jeglichen
        anderen Boni des Barbaren auf die Bewegung zu Land.
      </p>
      <p class="pb-6">
        <strong>Kampfrausch (AF):</strong> Ein Barbar kann seine innere Kraft
        und Wildheit freisetzen, um zusätzliche Kampfkraft zu gewinnen. Ab der
        1. Stufe kann ein Barbar für eine Anzahl von Runden am Tag in
        Kampfrausch verfallen, welche der Höhe seines KO-Modifikators +4
        entspricht. Beim Erreichen jeder weiteren Stufe verlängert sich der
        Kampfrausch um 2 zusätzliche Runden. Kurzfristige Erhöhungen des
        Konstitutionsattributs durch den Kampfrausch oder durch Zauber wie
        Ausdauer des Ochsen, erhöhen die Anzahl von Runden jedoch nicht, die der
        Barbar am Tag in Kampfrausch verfallen kann. Der Barbar kann sich mit
        Hilfe einer freien Aktion in den Kampfrausch versetzten. Die Gesamtzahl
        der Runden, die der Barbar am Tag in Kampfrausch sein kann, erneuert
        sich nach 8 Stunden Rast, wobei diese Rast nicht unbedingt an einem
        Stück gehalten werden muss. Während des Kampfrauschs erhält der Barbar
        einen Moralbonus von +4 auf Stärke und Konstitution, ebenso wie einen
        Moralbonus von +2 auf alle Willenswürfe. Dafür muss er einen Malus von –
        2 auf die Rüstungsklasse hinnehmen. Die Erhöhung des
        Konstitutionsattributs gibt dem Barbaren +2 zusätzliche Trefferpunkte
        pro Stufe, die jedoch am Ende des Rauschs wieder verschwinden und nicht
        wie temporäre Trefferpunkte zuerst abgezogen werden. Während des
        Kampfrauschs kann der Barbar weder Fertigkeiten benutzen, die auf
        Geschicklichkeit, Intelligenz oder Charisma basieren (außer Akrobatik,
        Einschüchtern, Fliegen oder Reiten), noch Fähigkeiten die besonderer
        Ruhe und Konzentration bedürfen. Der Barbar kann den Kampfrausch mit
        einer freien Aktion beenden und ist dann eine Anzahl von Runden
        erschöpft, die doppelt so hoch ist, wie die Anzahl der Runden, die der
        Barbar in Kampfrausch gewesen ist. Während der Barbar erschöpft ist,
        kann er nicht in einen weiteren Kampfrausch verfallen. Ansonsten ist es
        aber durchaus möglich, innerhalb des gleichen Kampfs mehrfach in den
        Kampfrausch zu gehen. Wenn ein Barbar ohnmächtig wird, endet sein
        Kampfrausch automatisch, wodurch er in Todesgefahr gerät.
      </p>
      <p class="py-12">
        <strong>Kampfrauschkräfte:</strong> Mit wachsender Erfahrung lernt ein
        Barbar, seinen Kampfrausch auf andere Arten zu nutzen. Auf der 2. Stufe
        erhält er eine Kampfrauschkraft und eine weitere alle zwei Stufen. Ein
        Barbar kann von Kampfrauschkräften nur dann profitieren, wenn er sich im
        Kampfrausch befindet, und einige dieser Kräfte erfordern es, dass der
        Barbar vorher eine bestimmte Aktion ausführt. Sofern nicht anders
        angegeben, kann er eine bestimmte Kraft nicht mehrmals auswählen.
      </p>
      <p class="pb-6">
        <em>Aberglaube (AF):</em> Der Barbar erhält einen Moralbonus von +2 auf
        Rettungswürfe, um Zaubern, übernatürlichen und zauberähnlichen
        Fähigkeiten zu widerstehen. Dieser Bonus erhöht sich um +1 für alle vier
        Stufen, die der Barbar erreicht hat. Im Kampfrausch kann der Barbar kein
        williges Ziel für einen Zauberspruch sein und muss den Rettungswurf
        gegen alle Zauber durchführen, auch gegen die von Verbündeten.
      </p>
      <p class="pb-6">
        <em>Dämmersicht (AF):</em> Die Sinne des Barbaren schärfen sich und er
        erhält während des Kampfrauschs Dämmersicht.
      </p>
      <p class="pb-6">
        <em>Einschüchterndes Niederstarren (AF):</em> Als Bewegungsaktion kann
        der Barbar einen Einschüchterungsversuch gegen einen angrenzenden Gegner
        machen. Ist er erfolgreich, demoralisiert er sein Opfer. Der Gegner ist
        für 1W4 Runden lang erschüttert +1 Runde für je 5 Punkte, die der Barbar
        beim Wurf über den SG kommt.
      </p>
      <p class="pb-6">
        <em>Erhöhte Schadensreduzierung (AF):</em> Die Schadensreduzierung des
        Barbaren erhöht sich um 1/-. Diese Schadensreduzierung wirkt nur im
        Kampfrausch. Ein Barbar kann diese Kraft bis zu drei Mal wählen. Ihre
        Auswirkungen addieren sich. Ein Barbar kann diese Kraft erst ab der 8.
        Stufe wählen.
      </p>
      <p class="pb-6">
        <em>Erneuerte Lebenskraft (AF):</em> Der Barbar kann als Standard-Aktion
        1W8 + seinen KO-Modifikator an Trefferpunkten heilen. Für alle vier
        Stufen, die der Barbar jenseits der 4. Stufe erreicht hat, erhöht sich
        die Menge des geheilten Schadens um 1W8 Punkte, bis zu einer Menge von
        5W8 auf der 20. Stufe. Um diese Kraft wählen zu können, muss der Barbar
        mindestens auf der 4. Stufe sein. Diese Kraft kann nur einmal am Tag und
        nur im Kampfrausch eingesetzt werden.
      </p>
      <p class="pb-6">
        <em>Furchtlose Wut (AF):</em> Im Kampfrausch ist der Barbar immun gegen
        die Zustände verängstigt und erschüttert. Der Barbar muss mindestens 12.
        Stufe sein, um diese Kraft zu wählen.
      </p>
      <p class="pb-6">
        <em>Geruchssinn (AF):</em> Der Barbar erhält während des Kampfrauschs
        die Eigenschaft Geruchssinn und kann diese einsetzen, um unsichtbare
        Feinde aufzuspüren.
      </p>
      <p class="pb-6">
        <em>Geweckte Wut (AF):</em> Der Barbar kann sich in einen Kampfrausch
        begeben, obwohl er erschöpft ist. Solange er mit Hilfe dieser Fähigkeit
        im Kampfrausch ist, kann er nicht erschöpft sein. Sobald der Kampfrausch
        schwindet, ist der Barbar für 10 Minuten je Runde entkräftet, die er im
        Kampfrausch gewesen ist.
      </p>
      <p class="pb-6">
        <em>Innere Zähigkeit (AF):</em> Im Kampfrausch ist der Barbar immun
        gegen die Zustände kränkelnd und Übelkeit. Der Barbar muss mindestens
        auf der 8. Stufe sein, um diese Kraft zu wählen.
      </p>
      <p class="pb-6">
        <em>Kampfschrei (AF):</em> Der Barbar kann als Standard-Aktion einen
        Kampfschrei ausstoßen. Alle Feinde innerhalb von 9 m müssen einen
        Willenswurf (SG entspricht 10 + ½ Stufe des Barbaren + seinem
        ST-Modifikator) bestehen oder sind 1W4+1 Runden in Panik. Musste ein
        Gegner einen Willenswurf gegen den Kampfschrei würfeln, kann er
        unabhängig vom Ergebnis für 24 Stunden nicht mehr Ziel derselben Kraft
        werden. Um diese Kraft zu wählen, muss der Barbar mindestens auf der 8.
        Stufe sein und schon über die Kraft Einschüchterndes Niederstarren
        verfügen.
      </p>
      <p class="pb-6">
        <em>Kein Entkommen (AF):</em> Der Barbar kann sich als Augenblickliche
        Aktion bis zum Doppelten seiner Bewegungsrate bewegen. Der Barbar kann
        diese Fähigkeit nur dann einsetzen, wenn ein benachbarter Gegner sich
        mit einer Rückzugsaktion aus dem Kampf entfernt. Der Barbar muss die
        Bewegung neben dem Feind beenden, der sich zurückgezogen hat. Die
        Bewegung des Barbaren verursacht normal Gelegenheitsangriffe. Diese
        Kraft kann nur einmal je Kampfrausch eingesetzt werden.
      </p>
      <p class="pb-6">
        <em>Klarer Augenblick (AF):</em> Für eine Runde erleidet der Barbar
        weder Vorteile noch Nachteile durch seinen Kampfrausch. Dies gilt auch
        für den Rüstungsklassenmalus und die beschränkte Auswahl an Aktionen.
        Das Aktivieren dieser Kraft ist eine Schnelle Aktion. Diese Runde zählt
        aber als Runde in Hinsicht auf die Gesamtrundenzahl, die dem Barbaren am
        Tag zur Verfügung stehen. Diese Kraft kann nur einmal im Kampfrausch
        eingesetzt werden.
      </p>
      <p class="pb-6">
        <em>Kraftrausch (AF):</em> Der Barbar addiert seine Barbarenstufe zu
        einer Stärke- oder Kampfmanöverprobe oder zu seiner
        Kampfmanöververteidigung, wenn ein Gegner ein Manöver gegen ihn
        versucht. Die Kraft wird als Augenblickliche Aktion genutzt. Diese Kraft
        kann nur einmal je Kampfrausch eingesetzt werden.
      </p>
      <p class="pb-6">
        <em>Kraftvoller Schlag (AF):</em> Der Barbar erhält einen Bonus von +1
        auf einen Schadenswurf. Dieser Bonus erhöht sich um +1 für alle vier
        Stufen, die der Barbar erreicht hat. Die Kraft wird als Schnelle Aktion
        eingesetzt, bevor der Angriff gewürfelt wurde. Diese Kraft kann nur
        einmal je Kampfrausch eingesetzt werden.
      </p>
      <p class="pb-6">
        <em>Mächtiger Schlag (AF):</em> Der Barbar kann einen Kritischen Treffer
        automatisch bestätigen. Diese Kraft kann als Augenblickliche Aktion
        genutzt werden, sobald der Barbar bei einem Angriff eine
        Bedrohungschance würfelt. Der Barbar muss auf der 12. Stufe sein, um
        diese Kraft auswählen zu können. Er kann diese Kraft nur einmal im
        Kampfrausch einsetzen.
      </p>
      <p class="pb-6">
        <em>Nachtsicht (AF):</em> Während des Rausches werden die Sinne des
        Barbaren außergewöhnlich geschärft und er erhält dann Dunkelsicht (18
        m). Um diese Kraft wählen zu können, muss der Barbar entweder durch sein
        Volk Dämmersicht haben oder die Kraft Dämmersicht gewählt haben.
      </p>
      <p class="pb-6">
        <em>Schnelle Reflexe (AF):</em> Während des Kampfrausches kann der
        Barbar einen zusätzlichen Gelegenheitsangriff pro Runde durchführen.
      </p>
      <p class="pb-6">
        <em>Schneller Schritt (AF):</em> Der Barbar erhält einen
        Verbesserungsbonus auf seine Bewegungsrate von 1,5 m. Diese Erhöhung ist
        im Kampfrausch immer aktiv. Der Barbar kann diese Kampfrauschkraft bis
        zu drei Mal wählen. Ihre Auswirkungen addieren sich.
      </p>
      <p class="pb-6">
        <em>Spontane Treffsicherheit (AF):</em> Der Barbar addiert einen
        Moralbonus von +1 zu einem Angriffswurf. Dieser Bonus erhöht sich um +1
        für alle vier Stufen, die der Barbar erreicht hat. Diese Kraft muss er
        als Schnelle Aktion vor dem Angriff einsetzen. Die Kraft kann nur einmal
        je Kampfrausch eingesetzt werden.
      </p>
      <p class="pb-6">
        <em>Starker Geist (AF):</em> Der Barbar kann einen misslungenen
        Willenswurf wiederholen. Diese Kraft wird als Augenblickliche Aktion
        eingesetzt, nachdem der Wurf gemacht worden ist, aber bevor der SL das
        Ergebnis bekannt gemacht hat. Der Barbar muss das Ergebnis des zweiten
        Wurfes nehmen, auch wenn es schlechter ist. Ein Barbar muss mindestens
        die 8. Stufe erreicht haben, um diese Kraft auswählen zu können. Diese
        Kraft kann nur einmal je Kampfrausch eingesetzt werden.
      </p>
      <p class="pb-6">
        <em>Unerwarteter Schlag (AF):</em> Der Barbar kann einen
        Gelegenheitsangriff gegen einen Gegner ausführen, der sich in ein Feld
        bewegt, das von dem Barbaren bedroht wird, unabhängig davon, ob diese
        Bewegung normalerweise einen Gelegenheitsangriff verursachen würde oder
        nicht. Diese Kraft kann nur einmal je Kampfrausch eingesetzt werden. Der
        Barbar muss mindestens auf der 8. Stufe sein, um diese Kraft zu wählen.
      </p>
      <p class="pb-6">
        <em>Verteidigungshaltung (AF):</em> Der Barbar erhält einen
        Ausweichbonus von +1 auf seine Rüstungsklasse gegen Fernkampfangriffe
        für eine Anzahl von Runden, die dem aktuellen KO-Modifikator entspricht
        (Minimum 1). Dieser Bonus erhöht sich um +1 für alle sechs Stufen, die
        der Barbar erreicht hat. Das Aktivieren dieser Eigenschaft ist eine
        Bewegungsaktion, die keinen Gelegenheitsangriff verursacht.
      </p>
      <p class="pb-6">
        <em>Wachsame Kampfhaltung (AF):</em> Der Barbar erhält einen
        Ausweichbonus von +1 auf seine Rüstungsklasse gegen Nahkampfangriffe für
        eine Anzahl von Runden, die dem aktuellen KO-Modifikator entspricht
        (Minimum 1). Dieser Bonus erhöht sich um +1 für alle sechs Stufen, die
        der Barbar erreicht hat. Das Aktivieren dieser Eigenschaft ist eine
        Bewegungsaktion, die keinen Gelegenheitsangriff verursacht.
      </p>
      <p class="pb-6">
        <em>Wuterfüllter Sprung (AF):</em> Im Kampfrausch addiert der Barbar
        seine Stufe als Verbesserungsbonus zu allen Fertigkeitswürfen in
        Akrobatik, die sich aufs Springen beziehen, und wird beim Sprung
        behandelt, als hätte er genügend Anlauf gehabt.
      </p>
      <p class="pb-6">
        <em>Wuterfülltes Klettern (AF): </em>Im Kampfrausch addiert der Barbar
        seine Stufe als Verbesserungsbonus zu allen Fertigkeitswürfen in
        Klettern.
      </p>
      <p class="pb-6">
        <em>Wuterfülltes Schwimmen (AF):</em> Im Kampfrausch addiert der Barbar
        seine Stufe als Verbesserungsbonus zu allen Fertigkeitswürfen in
        Schwimmen.
      </p>
      <p class="pb-12">
        <em>Zurücktreiben (AF):</em> Einmal pro Runde kann der Barbar anstelle
        eines Nahkampfangriffs versuchen, einen Ansturm zu unternehmen. Ist er
        erfolgreich, erleidet das Ziel Schaden in Höhe des ST-Modifikators des
        Barbaren und wird wie normal zurück geschoben. Der Barbar braucht sich
        nicht mit dem Ziel zurück zu bewegen, wenn er erfolgreich ist. Der
        Einsatz dieser Kraft verursacht keinen Gelegenheitsangriff.
      </p>
      <p class="pb-6">
        <strong>Reflexbewegung (AF):</strong> Ab der 2. Stufe kann der Barbar
        schon auf Bedrohungen reagieren, bevor seine Sinne es ihm eigentlich
        erlauben. Er kann nicht mehr auf dem falschen Fuß erwischt werden und
        verliert auch nicht seinen Geschicklichkeitsbonus auf die RK, wenn der
        Angreifer unsichtbar ist. Ein Barbar mit dieser Fähigkeit kann dennoch
