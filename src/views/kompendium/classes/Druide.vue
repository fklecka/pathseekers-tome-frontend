      <h3 class="text-2xl my-6 border-b">Klassenmerkmale</h3>
      <p>Die nachfolgenden Fähigkeiten sind Klassenmerkmale des Druiden.</p>
      <p class="pb-6">
        <strong>Umgang mit Waffen und Rüstungen:</strong> Der Druide ist im
        Umgang mit folgenden Waffen geübt: Dolch, Kampfstab, Keule, Krummsäbel,
        Kurzspeer, Schleuder, Sichel, Sense, Speer und Wurfpfeil. Er ist
        ebenfalls mit jeder natürlichen Waffe geübt, die ihm in Tiergestalt zur
        Verfügung steht (siehe Tiergestalt). Druiden sind mit dem Umgang
        leichter und mittelschwerer Rüstungen vertraut, dürfen aber keine
        Metallrüstung tragen, so dass sie die Auswahl zwischen Waffenrock,
        Lederrüstung oder Fellrüstung haben. Ein Druide kann auch hölzerne
        Panzerung tragen, die mit einem Zauber Eisenholz verändert wurde, was
        ihn wie Stahl schützt. Druiden können mit Schilden, außer Turmschilden,
        umgehen, dürfen aber auch nur Schilde einsetzen, die aus Holz
        hergestellt wurden.
      </p>
      <p class="pb-6">
        Ein Druide, der eine verbotene Rüstung trägt oder ein verbotenes Schild
        einsetzt, kann währenddessen und danach für weitere 24 Stunden weder
        zaubern, noch seine übernatürlichen und zauberähnlichen Klassenmerkmale
        anwenden.
      </p>
      <p class="pb-6">
        <strong>Zauber:</strong> Ein Druide wirkt göttliche Zauber, die er aus
        der Zauberliste für Druiden auswählt. Einige Zauber aus der Liste können
        jedoch der Ethik oder Moral seiner Gesinnung widersprechen und stehen
        ihm somit nicht zur Verfügung. Siehe hierzu „Böse, Chaotische, Gute und
        Rechtschaffene Zauber“. Ein Druide muss seine Zauber vorher auswählen
        und vorbereiten.
      </p>
      <p class="pb-6">
        Um einen Zauber vorbereiten und wirken zu können, muss der Druide
        mindestens eine Weisheit von 10 + Grad des Zaubers besitzen. Der
        Schwierigkeitsgrad des Rettungswurfs gegen die Zauber des Druiden ist 10
        + Grad des Zaubers + WE-Modifikator des Druiden.
      </p>
      <p class="pb-6">
        Wie andere Zauberkundige kann auch ein Druide nur eine bestimmte Anzahl
        von Zaubern pro Tag und Grad wirken. Die Zauber pro Tag sind in der
        obigen Tabelle: Druide aufgelistet. Zusätzlich erhält er noch
        Bonuszauber für die Höhe seines Attributs Weisheit.
      </p>
      <p class="pb-6">
        Der Druide verbringt eine Stunde am Tag in einer tranceähnlichen
        Meditation über die Mysterien der Natur, um seine Zauber wieder zu
        erlangen. Er kann jeden Zauber aus der Druidenliste wählen,
        vorausgesetzt ihm steht der Grad des Zaubers schon zur Verfügung, aber
        er muss während seiner Meditation auswählen, welche Zauber er für den
        Tag vorbereitet.
      </p>

      <p class="pb-6">
        <strong>Spontanes Zaubern:</strong> Ein Druide kann vorbereitete
        Zauberkraft nutzen, um Verbündete herbei zu zaubern. Der Druide kann
        einen seiner vorbereiteten Zauber aufgeben, um im gleichen oder einem
        niedrigeren Grad einen Verbündeten der Natur herbeizaubern zu wirken.
      </p>
      <p class="pb-6">
        <strong>Böse, Chaotische, Gute und Rechtschaffene Zauber:</strong> Ein
        Druide kann keinen Zauber wirken, dessen Gesinnung seiner oder der
        seiner Gottheit (wenn er einer folgt) entgegensteht. Zauber, die eine
        bestimmte Gesinnung voraussetzen, tragen eine entsprechende Bezeichnung
        in der Zauberbeschreibung.
      </p>
      <p class="pb-6">
        <strong>Gebet:</strong> Der Druide kann jeden Tag eine Anzahl von
        Gebeten, oder Zaubern des 0. Grades, vorbereiten, wie es der Angabe in
        der Tabelle: Druide unter „Zauber pro Tag“ entspricht. Diese Zauber
        werden wie andere Zauber gewirkt, nur dass sie keinen Zauberplatz
        verbrauchen und wieder verwendet werden dürfen.
      </p>
      <p class="pb-6">
        <strong>Bonussprachen:</strong> Ein Druide kann, zusätzlich zu der
        Auswahl seines Volks, Sylvanisch, die Sprache der Waldkreaturen, lernen.
        Der Druide lernt auf der 1. Stufe zudem die geheime Sprache Druidisch.
        Diese Sprache erhält der Druide zusätzlich zu seinen sonstigen Sprachen.
        Druiden ist es verboten, diese Sprache an andere weiterzugeben.
        Druidisch verfügt über ein eigenes Alphabet.
      </p>
      <p class="pb-6">
        <strong>Bund mit der Natur (AF):</strong> Ein Druide der 1. Stufe geht
        einen Bund mit der Natur ein. Dieser kann zwei verschiedene Formen haben
        und der Druide muss sich für eine entscheiden. Entweder entscheidet sich
        der Druide für einen engen Bund mit der Natur, der ihm Zugriff auf eine
        der Klerikerdomänen Erde, Feuer, Luft, Pflanzen, Tiere, Wasser oder
        Wetter gewährt. Um die Kräfte und Zauber dieser Domäne zu bestimmen,
        zählt die Stufe des Druiden wie die Stufe des Klerikers. Ein Druide, der
        diese Form des Bundes wählt, erhält außerdem einen zusätzlichen
        Zauberplatz für Domänenzauber, genau wie ein Kleriker. Er muss einen
        Domänenzauber in diesem Zauberplatz vorbereiten und kann diesen Zauber
        nicht einsetzen, um einen spontanen Zauber zu wirken.
      </p>
      <p class="pb-6">
        Die andere Möglichkeit für einen Bund ist die Wahl eines Tiergefährten.
        Ein Druide beginnt das Spiel mit einem Tiergefährten, der in der Sektion
        für Tiergefährten aufgeführt ist. Der Gefährte ist dem Druiden gegenüber
        loyal und begleitet ihn auf seine Abenteuer, wie es für seine Tierart
        angemessen ist.
      </p>
      <p class="pb-6">
        Anders als bei normalen Tieren steigen bei einem Tiergefährten die
        Trefferwürfel, seine Attribute, seine Fertigkeiten und seine Talente in
        einem ähnlichem Maße, wie auch der Druide neue Stufen erlangt. Sollte
        ein Charakter einen Tiergefährten aus mehr als einer Quelle erhalten,
        addieren sich seine effektiven Druidenstufen, wenn es darum geht, die
        Spielwerte und Fähigkeiten des Gefährten zu ermitteln. Die meisten
        Tiergefährten wachsen in ihrer Größe, wenn der Druide die 4. oder 7.
        Stufe erreicht, abhängig von der Tierart des Gefährten. Entlässt ein
        Druide einen Tiergefährten aus seinem Dienst, kann er nach einem Ritual
        von 24 Stunden ununterbrochenen Gebets in der Umgebung, wo die Tierart
        des neuen Gefährten normalerweise seinen Lebensraum hat, einen neuen
        Gefährten an sich binden. Mit einem solchen Ritual kann der Druide auch
        einen neuen Gefährten erlangen, wenn sein bisheriger Tiergefährte
        gestorben ist.
      </p>
      <p class="pb-6">
        <strong>Naturgespür (AF):</strong> Der Druide erhält einen
        Fertigkeitsbonus von +2 auf Würfe für Wissen (Natur) und
        Überlebenskunst.
      </p>
      <p class="pb-6">
        <strong>Tierempathie(AF):</strong> Ein Druide kann die Einstellung eines
        Tieres ihm gegenüber verbessern. Diese Fähigkeit funktioniert genau wie
        ein Wurf auf Diplomatie, um die Einstellung eines Menschen zu verändern
        (siehe Kapitel Fertigkeiten). Der Druide würfelt 1W20 und addiert sowohl
        seine Druidenstufe als auch seinen CH-Modifikator auf den Wurf. Ein
        zahmes Tier beginnt mit einer gleichgültigen Einstellung, während ein
        wildes Tier zunächst unfreundlich ist.
      </p>
      <p class="pb-6">
        Um Tierempathie einsetzten zu können, dürfen sich unter normalen
        Umständen Tier und Druide nicht weiter als 9 m von einander entfernt
        befinden. Im Allgemeinen dauert das Beeinflussen eines Tieres eine
        Minute. Ebenso wie bei Menschen kann es jedoch im Einzelfall auch einmal
        schneller oder langsamer vonstatten gehen.
      </p>
      <p class="pb-6">
        Ein Druide kann mit dieser Fähigkeit auch versuchen, Magische Bestien,
        die einen Intelligenzwert von 1 oder 2 haben, zu beeinflussen. Er nimmt
        dabei jedoch einen Malus von - 4 auf seinen Wurf hin.
      </p>
      <p class="pb-6">
        <strong>Unterholz durchqueren (AF):</strong> Ab der 2. Stufe kann sich
        ein Druide mit seiner vollen Bewegungsrate durch jede Art von Unterholz
        (wie natürliche Dornen, Gestrüpp, verwilderte Landschaften und ähnliches
        Terrain) bewegen, ohne Verletzungen oder Behinderungen zu erleiden.
        Dornen und anderes Unterholz, das magisch verändert wurde, um die
        Bewegung zu erschweren, betreffen den Druiden jedoch normal.
      </p>
      <p class="pb-6">
        <strong>Spurloser Schritt (AF):</strong> Ab der 3. Stufe hinterlässt der
        Druide in natürlicher Umgebung keine Spuren und kann auch mit
        Spurenlesen nicht verfolgt werden. Er kann sich aber durchaus dazu
        entscheiden, Spuren zu hinterlassen.
      </p>

      <p class="pb-6">
        <strong>Lockruf der Natur widerstehen (AF):</strong> Ab der 4. Stufe
        erhält der Druide einen Bonus von +4 auf Rettungswürfe gegen
        zauberähnliche und übernatürliche Fähigkeiten von Feenwesen. Dieser
        Bonus wirkt auch bei Zaubern, die Pflanzen betreffen, wie Dornenwuchs,
        Dürre, Holz krümmen und Verstricken.
      </p>
      <p class="pb-6">
        <strong>Tiergestalt (ÜF):</strong> Ab der 4. Stufe erhält der Druide die
        Fähigkeit, sich ein Mal pro Tag in ein kleines oder mittelgroßes Tier
        und wieder zurück zu verwandeln. Er kann sich hierbei für jede Kreatur
        mit der Bezeichnung Tier entscheiden. Diese Fähigkeit funktioniert, bis
        auf folgende Ausnahmen, genau wie der Zauber Bestiengestalt I. Die
        Verwandlung hält für eine Stunde pro Druidenstufe an, es sei denn, er
        verwandelt sich vorher zurück. Die Verwandlung (in ein Tier oder wieder
        zurück) ist eine Standard-Aktion und provoziert keine
        Gelegenheitsangriffe. Der Druide kann nur eine Tiergestalt wählen, die
        er kennt.
      </p>
      <p class="pb-6">
        In Tiergestalt verliert der Druide seine Fähigkeit zu sprechen und ist
        auf die Geräusche des untrainierten Tieres begrenzt. Er kann sich aber
        sehr wohl mit anderen Tieren seiner neuen Form verständigen. (Das
        natürliche Geräusch eines Papageis ist ein Kreischen, so dass der Druide
        auch in einer solchen Gestalt nicht sprechen kann.)
      </p>
      <p class="pb-6">
        Der Druide kann diese Fähigkeit ab der 6. und dann allen weiteren zwei
        Stufen ein weiteres Mal pro Tag nutzen, bis zu einem Maximum von acht
        Mal auf der 18. Stufe. Mit dem Erreichen der 20. Stufe kann sich der
        Druide jederzeit entscheiden, seine Gestalt zu ändern. Auf höheren
        Stufen ist er auch in der Lage, größere oder kleinere Tiergestalten oder
        sogar die von Pflanzen und Elementaren anzunehmen. Jede Verwandlung
        verbraucht, unabhängig von der Form, eine Anwendung der Fähigkeit.
      </p>
      <p class="pb-6">
        Ab der 6. Stufe kann der Druide sich ein großes oder sehr kleines Tier
        verwandeln oder die Gestalt eines kleinen Elementars annehmen. Wählt er
        die Gestalt eines Tieres, wirkt die Fähigkeit wie der Zauber
        Bestiengestalt II und im Falle des Elementars wirkt sie wie
        Elementargestalt I.
      </p>
      <p class="pb-6">
        Auf der 8. Stufe kann er sich in ein riesiges oder winziges Tier
        verwandeln. Stattdessen kann er sich auch zwischen einem mittelgroßen
        Elementar und einer kleinen oder mittelgroßen Pflanzenkreatur
        entscheiden. Diese Fähigkeiten entsprechen, je nach ihrer Art, den
        Zaubern Bestiengestalt III, Elementargetalt II oder Pflanzengestalt I.
      </p>
      <p class="pb-6">
        Ab der 10. Stufe kann sich der Druide für eine große Elementargestalt
        oder die einer großen Pflanzenkreatur entscheiden. Entscheidet er sich
        für den Elementar, funktioniert die Fähigkeit wie ein Elementargestalt
        III und im Falle der Pflanzenkreatur wie ein Pflanzengestalt II Zauber.
      </p>
      <p class="pb-6">
        Ab der 12. Stufe kann sich der Druide für eine riesige Elementargestalt
        oder die einer riesigen Pflanzenkreatur entscheiden. Entscheidet er sich
        für den Elementar, funktioniert die Fähigkeit wie der Zauber
        Elementargestalt IV und, im Falle der Pflanzenkreatur, wie ein
        Pflanzengestalt III.
      </p>
      <p class="pb-6">
        <strong>Immunität gegen Gifte (AF):</strong> Ab der 9. Stufe wird der
        Druide immun gegen jedes Gift.
      </p>
      <p class="pb-6">
        <strong>Tausend Gesichter (ÜF):</strong> Ab der 13. Stufe kann der
        Druide sein Aussehen jederzeit ändern. Die Fähigkeit wirkt wie der
        Zauber Gestalt verändern. Er kann dieses nur, solange er in seiner
        natürlichen Form ist.
      </p>
      <p class="pb-12">
        <strong>Zeitloser Körper (AF):</strong> Mit dem Erreichen der 15. Stufe
        nimmt der Druide keine Abzüge auf die Attribute durch das Altern mehr in
        Kauf und kann auch nicht auf magische Weise altern. Veränderungen, die
        er vorher erhalten hat, werden hier von nicht betroffen. Boni für das
        Alter erhält er weiterhin und er stirbt auch, wenn seine Zeit gekommen
        ist.
      </p>
      <p class="pb-6">
        <strong>Zaubertricks:</strong> Der Barde lernt eine Anzahl von
        Zaubertricks oder Zaubern des 0. Grads, wie in der Tabelle: Anzahl
        Bekannte Zauber angegeben ist. Diese Zauber werden wie andere Zauber
        gewirkt. Sie können jedoch beliebig oft am Tag gewirkt werden.
      </p>
      <p class="pb-6">
        <em>Wuterfülltes Schwimmen (AF):</em> Im Kampfrausch addiert der Barbar
        seine Stufe als Verbesserungsbonus zu allen Fertigkeitswürfen in
        Schwimmen.
      </p>
      <p class="pb-12">
        <strong>Vielseitiger Auftritt (AF):</strong> Auf der 2. Stufe kann der
        Barde eine Art der Fertigkeit Auftreten auswählen. Er kann den Bonus in
        dieser Fertigkeit anstelle seiner Boni in den dazugehörigen Fertigkeiten
        einsetzen. Wenn er auf diese Art und Weise seine Fertigkeit ersetzt,
        verwendet er den gesamten Fertigkeitsbonus, Bonus für einen Klassenbonus
        mit eingerechnet, egal ob die ersetzte Fertigkeit eine Klassenfertigkeit
        ist, oder der Barde darin noch keinen Rang hat. Auf der sechsten Stufe
        und alle weiteren vier Stufen kann der Barde eine weitere Art des
        Auftretens fürs Ersetzen auswählen. Die Arten des Auftretens und ihre
        dazugehörigen Fertigkeiten sind:
      </p>
      <ul class="pb-6">
        <li>Blasinstrumente (Diplomatie, Mit Tieren umgehen)</li>
        <li>Gesang (Bluffen, Motiv erkennen)</li>
        <li>Komik (Bluffen, Einschüchtern)</li>
        <li>Redekunst (Diplomatie, Motiv erkennen)</li>
        <li>Saiteninstrumente (Bluffen, Diplomatie)</li>
        <li>Schauspielkunst (Bluffen, Verkleiden)</li>
        <li>Schlaginstrumente (Mit Tieren umgehen, Einschüchtern)</li>
        <li>Tanzen (Akrobatik, Fliegen)</li>
        <li>Tasteninstrumente (Diplomatie, Einschüchtern)</li>
      </ul>

      <p class="pb-6">
        <strong>Bewandert (AF):</strong> Auf der 2. Stufe wird der Barde
        resistent gegen Bardenauftritte von anderen und gegen Schalleffekte
        generell. Er erhält einen Bonus von +4 auf Rettungswürfe gegen
        Bardenauftritt, Schallangriffe und sprachabhängige Effekte.
      </p>
      <p class="pb-6">
        <strong>Gelehrter (AF):</strong> Auf der 5. Stufe wird der Barde ein
        wahrer Gelehrter. Er kann bei jeder Wissensfertigkeit, in der er Ränge
        hat, 10 nehmen, wenn er dies wünscht. Ein Mal pro Tag kann der Barde bei
        einer Wissensprobe als Standard-Aktion 20 nehmen. Er kann dies für
        jeweils sechs Stufen über der 5. Stufe ein weiteres Mal pro Tag nutzen,
        bis zu einem Maximum von drei Mal auf der 17. Stufe.
      </p>
      <p class="pb-6">
        <strong>Tausendsassa (AF): </strong> Auf der 10. Stufe kann der Barde
        jede Fertigkeit nutzen, selbst wenn sie normalerweise nur trainiert
        genutzt werden darf. Ab der 16. Stufe zählen alle Fertigkeiten als
        Klassenfertigkeiten. Auf der 19. Stufe kann er für jede Fertigkeit 10
        nehmen, selbst wenn dies normalerweise nicht gestattet ist.
      </p>
      <h3 class="text-2xl my-6 border-b">Ehemalige Druiden</h3>
      <p class="pb-6">
        Ein Druide, der aufhört, die Natur zu verehren, zu einer verbotenen
        Gesinnung wechselt oder einem Nicht-Druiden Druidisch beibringt,
        verliert sowohl seine Zauber und Druidenfähigkeiten als auch seinen
        Tiergefährten. Die Fähigkeiten im Umgang mit Waffen und Rüstungen sind
        nicht betroffen. Er kann auch nicht weiter als Druide aufsteigen, bis er
        Buße getan hat (siehe den Zauber Buße).
      </p>
