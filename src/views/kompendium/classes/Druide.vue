      <p class="pb-6">
        <strong>Stufe:</strong> Dies ist die Druidenstufe des Charakters. Die
        Stufe als Druide wird zu allen seinen Stufen in Klassen addiert, denen
        ein Gefährte zusteht, um die Fähigkeiten des Gefährten zu bestimmen.
      </p>
      <p class="pb-6">
        <strong>TW:</strong> Dies ist die Gesamtzahl der achtseitigen (W8)
        Trefferwürfel, die der Tiergefährte hat. Zu jedem wird, wie normal auch,
        der KO-Modifikator addiert.
      </p>
      <p class="pb-6">
        <strong>GAB:</strong> Dies ist der Grund-Angriffsbonus des
        Tiergefährten. Der Grund-Angriffsbonus des Tiergefährten entspricht dem
        Grund-Angriffsbonus eines Druiden von der Stufe, wie der Tiergefährte
        Trefferwürfel (TW) hat. Tiergefährten erhalten keine zusätzlichen
        Attacken aufgrund eines hohen Grund-Angriffsbonus bei ihren Angriffen
        mit natürlichen Waffen.
      </p>
      <p class="pb-6">
        <strong>REF / WIL / ZÄH:</strong> Dies sind die Basisrettungswürfe für
        den Tiergefährten. Ein Tiergefährte hat gute Reflex- und
        Zähigkeitswürfe.
      </p>
      <p class="pb-6">
        <strong>Fertigkeiten:</strong> Dies beschreibt die Gesamtzahl an
        Fertigkeitsrängen, die der Tiergefährte hat. Tiergefährten können diese
        Ränge auf alle Fertigkeiten verteilen, die unter Tierfertigkeiten
        aufgeführt sind. Sollte ein Tiergefährte seine Intelligenz auf 10 oder
        höher steigern, erhält er Bonusränge auf Fertigkeiten, wie normal auch.
        Tiergefährten mit einer Intelligenz von 3 oder mehr können Ränge in
        beliebigen Fertigkeiten erwerben. Ein Tiergefährte kann nicht mehr Ränge
        in einer Fertigkeiten haben, als er Trefferwürfel hat.
      </p>
      <p class="pb-6">
        <strong>Talente:</strong> Hierbei handelt es sich um die Gesamtzahl an
        Talenten, die ein Tiergefährte hat. Tiergefährten sollten ihre Talente
        aus der Liste auswählen, die unter Tiertalente angegeben ist. Sie können
        auch andere Talente wählen, aber sie sind teilweise nicht in der Lage
        diese Talente einzusetzen (wie z.B. Umgang mit Kriegswaffen). Außerdem
        dürfen Tiergefährten ein Talent mit der Vorraussetzung eines
        Grund-Angriffsbonus von +1 (oder besser) nicht als erstes Talent wählen
        (sondern erst mit dem zweiten Talent, welches sie auf der 2. Stufe mit 3
        Trefferwürfeln erhalten).
      </p>
      <p class="pb-6">
        <strong>Natürliche Rüstung:</strong> Die angegebene Zahl ist zusätzlich
        zu der vorhanden natürlichen Rüstung des Tieres zu verstehen.
      </p>
      <p class="pb-6">
        <strong>ST / GE:</strong> Addiere diese Zahl zu den Werten in Stärke und
        Geschicklichkeit des Tieres.
      </p>
      <p class="pb-6">
        <strong>Bonustricks:</strong> Der Wert in dieser Spalte ist die
        Gesamtzahl an „Bonustricks“, die der Tiergefährte neben normalen Tricks
        kennt, die der Druide seinem Gefährten mit der Fertigkeit Mit Tieren
        umgehen beigebracht haben könnte (siehe die Fertigkeit Mit Tieren
        umgehen für weitere Details, wie man einem Tier Tricks beibringt). Diese
        zusätzlichen Tricks benötigen keine Zeit zum Abrichten oder
        Fertigkeitswürfe auf Mit Tieren umgehen und sie zählen auch nicht gegen
        die Gesamtzahl an Tricks, die das Tier kennen kann. Der Druide wählt die
        Tricks aus und kann diese Wahl danach nicht mehr ändern.
      </p>
      <p class="pb-6">
        <strong>Speziell:</strong> Dies schließt eine Anzahl von Fähigkeiten
        ein, die ein Tiergefährte erlangt, wenn er mächtiger wird. Die
        verschiedenen Fähigkeiten werden unten beschrieben.
      </p>
      <p class="pb-6">
        <strong>Verbindung (AF):</strong> Der Druide kann mit dem Gefährten als
        Freie Aktion umgehen und ihn mit einer Bewegungsaktion antreiben, selbst
        wenn er keinen Rang in Mit Tieren umgehen haben sollte. Der Druide
        erhält einen Situationsbonus von +4 auf alle Würfe auf Tierempathie und
        Mit Tieren umgehen, die den Gefährten betreffen.
      </p>
      <p class="pb-6">
        <strong>Zauber teilen (AF):</strong> Der Druide kann Zauber, die er nur
        auf sich selber sprechen könnte, auch auf den Gefährten wirken (als
        Zauber mit der Reichweite Berührung). Dies können auch Zauber sein, die
        normalerweise keinen Effekt auf Tiere haben. Zauber, die auf diese Art
        gewirkt werden, müssen von einer Klasse kommen, die einen Tiergefährten
        gewährt. Diese Fähigkeit erlaubt es nicht, mit einem Tiergefährten
        Eigenschaften zu teilen, die keine Zauber sind, auch wenn sie wie Zauber
        funktionieren.
      </p>
      <p class="pb-6">
        <strong>Entrinnen (AF):</strong> Ist der Gefährte einem Angriff
        ausgesetzt, der einen Reflexwurf für halben Schaden erlaubt, erhält der
        Gefährte bei gelungenem Rettungswurf keinen Schaden.
      </p>
      <p class="pb-6">
        <strong>Erhöhung eines Attributwertes (AF):</strong> Der Tiergefährte
        kann auf eines seiner Attribute +1 hinzuzählen.
      </p>
      <p class="pb-6">
        <strong>Hingabe (AF):</strong> Der Gefährte erhält einen Moralbonus von
        +4 auf Willenswürfe gegen Verzauberungen und Verzauberungseffekte.
      </p>
      <p class="pb-6">
        <strong>Mehrfachangriff:</strong> Der Gefährte erhält, sofern er es
        nicht schon hat, Mehrfachangriff als Bonustalent, wenn er über drei oder
        mehr natürliche Angriffe verfügt. Hat er keine drei natürlichen
        Angriffe, erhält er einen zusätzlichen Angriff mit einer seiner
        natürlichen Waffen, wenn auch mit einem Malus von – 5.
      </p>
      <p class="pb-6">
        <strong>Verbessertes Entrinnen (AF):</strong> Ist der Gefährte einem
        Angriff ausgesetzt, der einen Reflexwurf für halben Schaden erlaubt,
        erhält der Gefährte bei gelungenem Rettungswurf keinen und bei
        misslungenem Wurf nur halben Schaden.
      </p>
      <h3 class="text-2xl my-6 border-b">Tierfertigkeiten</h3>
      <p class="pb-6">
        Tiergefährten können Ränge in jeder der folgenden Fertigkeiten erlangen:
        Akrobatik* (GE), Einschüchtern (CH), Entfesselungskunst (GE), Fliegen*
        (GE), Heimlichkeit* (GE), Klettern* (ST), Schwimmen* (ST),
        Überlebenskunst (WE) und Wahrnehmung* (WE). Alle Fertigkeiten, die mit
        einem (*) versehen sind, gelten für Tiergefährten als
        Klassenfertigkeiten.
      </p>
      <p class="pb-6">
        Tiergefährten mit einer Intelligenz von 3 oder mehr können Ränge in
        beliebige Fertigkeiten verteilen.
      </p>
      <h3 class="text-2xl my-6 border-b">Tiertalente</h3>
      <p class="pb-6">
        Tiergefährten können aus folgenden Talenten auswählen: Abhärtung,
        Akrobat, Athlet, Ausdauer, Ausweichen, Beweglichkeit, Blind kämpfen,
        Blitzschnelle Reflexe, Einschüchternde Kraft, Eiserner Wille,
        Fertigkeitsfokus, Flinke Manöver, Große Zähigkeit, Heftiger Angriff,
        Kampfreflexe, Rennen, Tänzelnder Angriff, Umgang mit Rüstungen
        (leichte), Umgang mit Rüstungen (mittelschwere), Umgang mit Rüstungen
        (schwere), Unverwüstlich, Verbesserte Initiative, Verbesserte natürliche
        Rüstung, Verbesserter Ansturm, Verbesserter natürlicher Angriff,
        Verbessertes Überrennen, Verstohlenheit, Waffenfinesse und Waffenfokus.
      </p>
      <h3 class="text-2xl my-6 border-b">Auswahl einer Tierart</h3>
      <p class="pb-6">
        Jeder Tiergefährte hat zu Beginn von seiner Tierart her unterschiedliche
        Größen, Bewegungsraten, Angriffe, Attribute und spezielle Fähigkeiten.
        Alle Angriffe eines Tiergefährten werden normalerweise mit seinem vollen
        Grund-Angriffsbonus durchgeführt (außer es steht anderes dabei).
        Angriffe eines Tieres addieren ihren ST-Modifikator zum Schadenswurf,
        außer es handelt sich um den einzigen Angriff. In diesem Fall wird das
        Anderthalbfache des ST-Modifikators hinzuaddiert. Einige haben besondere
        Fähigkeiten, wie zum Beispiel Geruchssinn. Siehe dazu den Anhang 1 für
        weitere Informationen über diese Fähigkeiten. In dem Maße, in dem du
        weitere Stufen erlangst, wird auch dein Tiergefährte besser. Neben den
        normalen Boni, die in der Tabelle vermerkt sind, gibt es auf der 4. oder
        7. Stufe einen zusätzlichen Aufstieg. Anstelle des Aufstiegs, wie er für
        die 4. oder 7. Stufe angegeben ist, kannst du auch die Wahl treffen, die
        Geschicklichkeit und Konstitution deines Gefährten um +2 zu erhöhen.
      </p>
